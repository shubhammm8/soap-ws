package ws.kalinga;

public class Lead {
	private int lid;
	private String LeadName;
	private int Tid;
	public int getLid() {
		return lid;
	}
	public void setLid(int lid) {
		this.lid = lid;
	}
	public String getLeadName() {
		return LeadName;
	}
	public void setLeadName(String leadName) {
		LeadName = leadName;
	}
	public int getTid() {
		return Tid;
	}
	public void setTid(int tid) {
		Tid = tid;
	}

}
